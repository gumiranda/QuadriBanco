    private void jbcriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcriarActionPerformed
        Conta c = new Conta();
       String nome = jtnome1.getText();
       Cliente c1 = BaseDadosClientes.buscar(nome); // verifica se o cliente esta cadastrado
       if(c1!=null){
           c.setCliente(c1);
           c.setNum_Conta(jtnumconta.getText());
           c.setId_cliente(jtId.getText());
           BaseDadosContas.adicionar(c);
           BaseDadosContas.cadastra(c,"contas.dat");
           JOptionPane.showMessageDialog(this,"Conta criada com sucesso!");
           
       }
       else{
            JOptionPane.showMessageDialog(this,"Erro ao criar conta, tente novamente!");
          
       }
       getContentPane().remove(jbcriar);
       TelaCriaConta.this.dispose();
       jbcriar = null;  
    }//GEN-LAST:event_jbcriarActionPerformed

